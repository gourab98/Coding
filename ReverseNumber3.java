public class ReverseNumber3 {
    
    public static void main(String [] args){
     
        for(int i=1000;i>0;)
        {
            System.out.print(i+"\t");
            i=i-1;
            System.out.print(i+"\t");
            i=i-1;
            System.out.print(i+"\t");
            i=i-1;
            System.out.print(i+"\t");
            i=i-1;
            System.out.println(i+"\t");
            i=i-1;

        }
    }
}
